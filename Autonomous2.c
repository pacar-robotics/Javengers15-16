void initializeRobot()
{
	servo[Gate] = GATE_CLOSED;
	servo[Hooks] = GOAL_HOOKS_OPEN;
	initSensor(&irSeeker, S2);
	wait1Msec(100);
	nMotorEncoder[LeftWheels] = 0;
	nMotorEncoder[RightWheels] = 0;
}

